package basic.ch04;

public class IfElseTest {

	public static void main(String[] args) {
		
		int age = 20;
		
		if (age >= 20) {
			System.out.println("고등학교를 졸업했습니다.");
			// 크거나 같다면 위 출력
		} else { 
			System.out.println("학교를 다니고 있습니다.");
			//거짓이라면 위 출력 
		}
		// if else 구문은 반드시 하나는 실행됨! 
		
		
	} // end  of main

} // end of class
